import requests
import json
import time
import sys

BASE_URL = "https://biblie-school-backend.vercel.app/api/v1"
TOKEN = sys.argv[1] if len(sys.argv) > 1 else ""

HEADERS = {
    "Authorization": f"Bearer {TOKEN}",
    "Content-Type": "application/json"
}


def api(method, path, data=None):
    url = f"{BASE_URL}{path}"
    r = getattr(requests, method)(url, headers=HEADERS, json=data, timeout=30)
    if r.status_code >= 400:
        print(f"  ERROR {r.status_code}: {r.text[:300]}")
        return None
    return r.json()


def create_course():
    print("=" * 60)
    print("CREATING COURSE: Книга Деяний Апостолов")
    print("=" * 60)

    course = api("post", "/courses", {
        "title": "Книга Деяний Апостолов: От Иерусалима до края земли",
        "description": (
            "Как горстка учеников превратилась в движение, изменившее мир? "
            "Этот курс — глубокое, но доступное путешествие через все 28 глав книги Деяний. "
            "Вы увидите рождение Церкви в день Пятидесятницы, проследите миссионерские путешествия "
            "апостола Павла от Иерусалима до Рима, и откроете принципы, актуальные для Церкви сегодня. "
            "4 модуля · ~4.5 часа · мини-квизы · финальный экзамен на 100 вопросов · сертификат"
        ),
    })
    if not course:
        print("FATAL: Could not create course")
        sys.exit(1)

    course_id = course["id"]
    print(f"  Course created: {course_id}")
    return course_id


