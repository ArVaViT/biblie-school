def create_modules_and_chapters(course_id):
    created = {}
    for mod_data in MODULES:
        print(f"\n--- Creating Module: {mod_data['title']}")
        mod = api("post", f"/courses/{course_id}/modules", {
            "title": mod_data["title"],
            "description": mod_data["description"],
            "order_index": mod_data["order_index"],
        })
        if not mod:
            continue
        mod_id = mod["id"]
        print(f"  Module ID: {mod_id}")
        created[mod_data["order_index"]] = {"id": mod_id, "chapters": {}}

        for ch_data in mod_data["chapters"]:
            print(f"    Creating Chapter: {ch_data['title']}")
            ch = api("post", f"/courses/{course_id}/modules/{mod_id}/chapters", {
                "title": ch_data["title"],
                "chapter_type": ch_data["chapter_type"],
                "order_index": ch_data["order_index"],
            })
            if not ch:
                continue
            ch_id = ch["id"]
            print(f"      Chapter ID: {ch_id}")
            created[mod_data["order_index"]]["chapters"][ch_data["order_index"]] = ch_id

            for i, block in enumerate(ch_data.get("content_blocks", [])):
                print(f"      Creating Block {i}...")
                api("post", f"/blocks/chapter/{ch_id}", {
                    "block_type": block["block_type"],
                    "order_index": i,
                    "content": block.get("content"),
                    "video_url": block.get("video_url"),
                })
            time.sleep(0.3)

    return created


