def mcq(
    text: str,
    *,
    correct: str,
    wrong1: str,
    wrong2: str,
    wrong3: str,
    order_index: int,
    points: int = 1,
) -> dict:
    """Shortcut for a single-correct multiple-choice question."""
    return {
        "question_text": text,
        "question_type": "multiple_choice",
        "order_index": order_index,
        "points": points,
        "options": [
            {"option_text": correct, "is_correct": True, "order_index": 0},
            {"option_text": wrong1, "is_correct": False, "order_index": 1},
            {"option_text": wrong2, "is_correct": False, "order_index": 2},
            {"option_text": wrong3, "is_correct": False, "order_index": 3},
        ],
    }


def tf(text: str, *, correct_true: bool, order_index: int, points: int = 1) -> dict:
    return {
        "question_text": text,
        "question_type": "true_false",
        "order_index": order_index,
        "points": points,
        "options": [
            {"option_text": "Верно", "is_correct": correct_true, "order_index": 0},
            {"option_text": "Неверно", "is_correct": not correct_true, "order_index": 1},
        ],
    }


def essay(text: str, *, order_index: int, points: int = 1) -> dict:
    return {
        "question_text": text,
        "question_type": "short_answer",
        "order_index": order_index,
        "points": points,
        "options": [],
    }



